package com.domain;

public class ProvinceBean {
    public String date;
    public String addConfirmCount;
    public String totalConfirmCount;
    public String cured;
    public String death;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAddConfirmCount() {
        return addConfirmCount;
    }

    public void setAddConfirmCount(String addConfirmCount) {
        this.addConfirmCount = addConfirmCount;
    }

    public String getTotalConfirmCount() {
        return totalConfirmCount;
    }

    public void setTotalConfirmCount(String totalConfirmCount) {
        this.totalConfirmCount = totalConfirmCount;
    }

    public String getCured() {
        return cured;
    }

    public void setCured(String cured) {
        this.cured = cured;
    }

    public String getDeath() {
        return death;
    }

    public void setDeath(String death) {
        this.death = death;
    }
}
